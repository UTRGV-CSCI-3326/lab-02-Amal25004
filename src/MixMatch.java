public class MixMatch
{
    public static void main(String[] args)
    {
        String name = "Angel";
        char letter = 'A';
        short numShort = 1;
        int numInt = 3;
        long numLong = 7;
        double numDouble = 9.53;
        boolean bool = true;

        System.out.println("Single print method: ");
        System.out.println("Name: " + name);
        System.out.println("Char: " + letter);
        System.out.println("Short: " + numShort);

        System.out.println("Multiple variables method:");
        System.out.println("Int: " + numInt + " Long: " + numLong + " Float: " + numLong + " Double: " + numDouble + " Boolean: " + bool);
    }
}